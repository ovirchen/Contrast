#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned int	i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char*)s;
	while (i < n)
		str[i++] = c;
	return (s);
}
